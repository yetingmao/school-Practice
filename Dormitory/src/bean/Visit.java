package bean;

public class Visit {
	private String visitername;
	private String id;
	private String visitedname;
	private String come;
	private String leave;
	private String RoomID;
	public String getVisitername() {
		return visitername;
	}
	public void setVisitername(String visitername) {
		this.visitername = visitername;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getVisitedname() {
		return visitedname;
	}
	public void setVisitedname(String visitedname) {
		this.visitedname = visitedname;
	}
	public String getCome() {
		return come;
	}
	public void setCome(String come) {
		this.come = come;
	}
	public String getLeave() {
		return leave;
	}
	public void setLeave(String leave) {
		this.leave = leave;
	}
	public String getRoomID() {
		return RoomID;
	}
	public void setRoomID(String roomID) {
		RoomID = roomID;
	}

}
